#include "visdef.h"                     /* Standard type definitions    */
#include "..\genpcm.h"                  /* PCM conversion routine hdr   */
#include "xltsim.h"                     /* Translation simulator defs   */

#include <stdlib.h>                     /* Misc string/math/error funcs */
#include <stdio.h>                      /* Standard I/O                 */

/************************************************************************/
/************************************************************************/
#define STPTABSIZ   49

/************************************************************************/
/************************************************************************/
void main ()
{
    int i, j;
    extern short StpTab[];

    /********************************************************************/
    /********************************************************************/
    printf ("static short _based (_segname (\"_CODE\")) uLFToW[] = {\n");
    for (j=0; j<32; j++) {
        for (i=0; i<8; i++) {
            printf ("\t%5d,", uLFToW ((BYTE) (8*j + i)));
        }
        printf ("\n");
    }
    printf ("};\n\n");

    /********************************************************************/
    /********************************************************************/
    printf ("static BYTE _based (_segname (\"_CODE\")) WavToF[] = {\n");
    for (j=0; j<256; j++) {
        for (i=0; i<8; i++) {
            printf ("\t0x%2x,", (BYTE) WavToF (8*j + i));
        }
        printf ("\n");
    }
    printf ("};\n\n");


    /********************************************************************/
    /********************************************************************/
    printf ("static BYTE _based (_segname (\"_CODE\")) uLFOrd[] = {\n");
    for (j=0; j<32; j++) {
        for (i=0; i<8; i++) {
            printf ("\t0x%02x,", (BYTE) uLFOrd ((BYTE) (8*j + i)));
        }
        printf ("\n");
    }
    printf ("};\n\n");

    /********************************************************************/
    /********************************************************************/
    exit (0);

}
